public class RandomPractice{
	public static void main(String[] arg){
		if (Math.random() < .5){
			System.out.println("Heads");
		}else{
			System.out.println("Tails");
		}
		
		if(Math.random() < .3){
			myColor = color(0,0,225);
		}else{
			myColor = color(225,0,0);
		System.out.println(myColor);
		}
		
	}
}